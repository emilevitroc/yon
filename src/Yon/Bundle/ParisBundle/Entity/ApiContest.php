        $this->creation = $creation;

        return $this;
    }

    /**
     * Get creation
     *
     * @return \DateTime 
     */
    public function getCreation()
    {
        return $this->creation;
    }

    /**
     * Set name
     *
     * @param string $name
     * @return ApiContest
     */
    public function setName($name)

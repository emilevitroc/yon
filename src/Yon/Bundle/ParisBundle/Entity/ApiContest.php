    /**
     * @var string
     *
     * @ORM\Column(name="description", type="string", length=256, nullable=true)
     */
    private $description;

    /**
     * @var integer
     *
     * @ORM\Column(name="planned_challenges_count", type="integer", nullable=true)
     */
    private $plannedChallengesCount;


        $this->plannedChallengesCount = $plannedChallengesCount;

        return $this;
    }

    /**
     * Get plannedChallengesCount
     *
     * @return integer 
     */
    public function getPlannedChallengesCount()
    {
        return $this->plannedChallengesCount;
    }

